public class Conditional {
    public static void main(String ... args){
        char fruit ='a';
        switch(fruit){
            case 'm':
                System.out.println("MANGO");
                break;
            case 'a':
                System.out.println("APPLE");
                break;
            case 'p':
                System.out.println("PINEAPPLE");
                break;
            case 's':
                System.out.println("STRAWBERRY");
                break;
        }
    }
}
